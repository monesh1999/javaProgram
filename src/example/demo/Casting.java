package example.demo;

public class Casting {
	public static void main(String[] args) {
		
		
		int num = 10;
		double result = num; // Automatic conversion
		
		System.out.println(num);
		System.out.println(result); 
		
		double res = 9.88d;
		int my = (int) res;
		System.out.println(res);
		System.out.print(my);

	}

}
